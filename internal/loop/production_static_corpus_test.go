func TestStaticProductionTraceCorpusBehavior(t *testing.T) {
	totalObservations := 0
	runawayCount := 0
	legitCount := 0

	for _, trace := range staticProductionTraceCorpus {
		trace := trace
		totalObservations += len(trace.observations)
		if trace.label == "true_runaway" {
			runawayCount++
		} else {
			legitCount++
		}

		t.Run(trace.name, func(t *testing.T) {
			result := replayProductionTrace(trace.observations)
			if result.final.ActionCeiling != trace.expectedFinal {
				t.Fatalf("final action=%s want %s confidence=%.2f signals=%v first_block_step=%d", result.final.ActionCeiling, trace.expectedFinal, result.final.Confidence, result.final.SignalsFired, result.firstBlockStep)
			}
			if trace.minConfidence > 0 && result.final.Confidence < trace.minConfidence {
				t.Fatalf("confidence %.2f below minimum %.2f signals=%v", result.final.Confidence, trace.minConfidence, result.final.SignalsFired)
			}
			if trace.maxConfidence > 0 && result.final.Confidence > trace.maxConfidence {
				t.Fatalf("confidence %.2f above maximum %.2f signals=%v", result.final.Confidence, trace.maxConfidence, result.final.SignalsFired)
			}
			if trace.maxBlockStep > 0 && (result.firstBlockStep == 0 || result.firstBlockStep > trace.maxBlockStep) {
				t.Fatalf("first block step=%d want<=%d confidence=%.2f signals=%v", result.firstBlockStep, trace.maxBlockStep, result.final.Confidence, result.final.SignalsFired)
			}
		})
	}

	if totalObservations < 300 {
		t.Fatalf("static production corpus too small: observations=%d want>=300", totalObservations)
	}
	if runawayCount < 25 || legitCount < 20 {
		t.Fatalf("static production corpus lacks balance: runaways=%d legit=%d", runawayCount, legitCount)
	}
}

func TestStaticProductionTraceCorpusSourceLineBudget(t *testing.T) {
	_, file, _, ok := runtime.Caller(0)
	if !ok {
		t.Fatal("runtime.Caller failed")
	}
	f, err := os.Open(file)
	if err != nil {
		t.Fatal(err)
	}
	defer f.Close()

	lines := 0
	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		lines++
	}
	if err := scanner.Err(); err != nil {
		t.Fatal(err)
	}
	if lines < 2000 {
		t.Fatalf("static production corpus source too small: lines=%d want>=2000", lines)
	}
}

